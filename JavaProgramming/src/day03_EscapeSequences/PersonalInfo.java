package day03_EscapeSequences;

public class PersonalInfo {

    public static void main(String[] args) {

        System.out.println("Name: Mia");
        System.out.println("Gender: F");
        System.out.println("Date Of Birth: 08.01.1989");
        System.out.println("Address: 17 Canterbury Road,UK");
        System.out.println("Phone: 07964256212");
        System.out.println("SSN: (***)-***-**87");

    }


}
/*
Name: Mia
Gender: F
Date Of Birth: 08.01.1989
Address: 17 Canterbury Road,UK
Phone: 07964256212
SSN: (***)-***-**87
 */