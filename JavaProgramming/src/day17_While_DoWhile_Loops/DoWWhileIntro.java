package day17_While_DoWhile_Loops;

public class DoWWhileIntro {
    public static void main(String[] args) {

        boolean a = false;

        while (a){
            System.out.println("Wooden Spoon");
        }

        System.out.println("==============");

        do{
            System.out.println("Wooden Spoon");
        }while(a);

    }
}
