package day26;

public class SubtractionOfNumbers {
    
    public static int subtraction(int num1, int num2){
        
        int result = num1 - num2;
        return result;
    }
    public static double subtraction(double num1, double num2){

        double result = num1 - num2;
        return result;
    }


}
