package day26;

public class SumOfNumbers {

    public static int sumNumbers(int num1, int num2){
       int sum = num1 + num2;
       return sum;
    }
    public static double sumNumbers(double num1, double num2){
        double sum = num1 + num2;
        return sum;
    }
}
